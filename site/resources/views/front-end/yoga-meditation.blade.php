        <p>
          The whole system of Yoga is built on three main structures: exercise, breathing, and meditation. The exercises of Yoga are designed to put pressure on the glandular systems of the body, thereby increasing its efficiency and total health. The body is looked upon as the primary instrument that enables us to work and evolve in the world, and so a Yoga student treats it with great care and respect. Breathing techniques are based on the concept that breath is the source of life in the body. The Yoga student gently increases breath control to improve the health and function of both body and mind. These two systems of exercise and breathing then prepare the body and mind for meditation, and the student finds an easy approach to a quiet mind that allows silence and healing from everyday stress. Regular daily practice of all three parts of this structure of Yoga produce a clear,  bright mind and a strong, capable body.
        </p>
        <h4 class="text-uppercase">Types of Yoga:</h4>
        <p>
          There are over a hundred different schools of Yoga. Some of the most well known are described below:
        </p>
        <p>
          <b>Hatha Yoga:</b> The physical movements and postures, plus breathing techniques.This is what most people associate with Yoga practice.
        </p>
        <p>
          <b>Raja Yoga:</b> Called the “royal road,” because it incorporates exercise and breathing practice with meditation and study, producing a well-rounded individual.
        </p>
        <p>
          <b>Jnana Yoga:</b> The path of wisdom; considered the most difficult path.
        </p>
        <p>
          <b>Bhakti Yoga:</b>The practice of extreme devotion in one-pointed concentration upon one’s concept of God.
        </p>
        <p>
          <b>Karma Yoga:</b>All movement, all work of any kind is done with the mind centered on a personal concept of God.
        </p>
        <p>
          <b>Tantra Yoga:</b>A way of showing the unseen consciousness in form through specific words, diagrams, and movements. One of the diagrams that is used to show the joining of the physical and spiritual bodies is two triangles superimposed upon one another. The downward-pointing triangle represents the physical body, or the female aspect having to do with work, action, and movement; the upward-pointing triangle represents the spiritual body of support, energy, and vastness.
        </p>
        <p>
          <b>Kashmir Shaivism:</b>This Yoga system states that everything in the universe has both male and female qualities. In Kashmir Shaivism, these male and female principles form an equal partnership, so interdependent that they cannot be separated. The attraction between them produces the ultimate union of opposites, creating the immense complexity of the universe that we enjoy and celebrate. Unlike other philosophies, Kashmir Shaivism is based in emotion rather than intellect. In fact, Shaivism says that intellectual understanding by itself will never lead us to the realization of the summit of Yoga. The system's great exponents teach that the egotistical intellect blocks our ability to fully experience our individual power.
